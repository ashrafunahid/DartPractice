class MyClass{
  // If we want to use property of any class than we have to make a instance variable
  // of that class. and after that we can use any property of that class through that
  // instance.
  // But if we use static keyword before any method name than we can directly call
  // that method without making any instance of that class.
  static void MyFunc(){
    print('This is example of using static keyword!');
  }
}